  <Title>Did you know?</Title>
  <Content>
    Certain terms and phrases at the core of the "burner" identity are actually intellectual property, and that to use them we need to license them from The Burning Man Project? Things like "Burning Man," "Decompression Party," and all of the Ten Principles.
    To license this stuff, we agree to behave within certain cultural norms defined by The Burning Man Project. This agreement does not work both ways, and licensors have no control over the brand that they have licensed or the behaviour of The Burning Man Project itself.
